def get_area(r , pi = 3.14159):
    """Doiraning yuzini qaytaruvchi funktsiya"""
    return pi*(r**2)
def get_perimeter(r , pi = 3.14159):
    """Doiraning perimetrining qaytaruvchi funktsiya"""
    return 2*pi*r



